;; ## Why this matters
;; In production, we observed a **5x regression** in page load times after deploying
;; `not-join`-based relationship sensitivity filtering in XTDB 1.x. This notebook
;; compares three filtering approaches across multiple data scales to understand
;; the performance characteristics and identify the optimal strategy.
