;; ## Conclusion
;;
;; **Two-phase wins** across all scales because:
;;
;; 1. **Phase 1** (find excluded IDs) is a single index scan returning a tiny set
;; 2. **Phase 2** (filter logs) uses O(1) set membership — no DB joins needed
;; 3. **not-join** in XTDB 1.x evaluates as a nested loop (O(N x M)), which
;;    becomes increasingly expensive as data grows
;;
;; The post-filter approach is comparable to two-phase at small scales but requires
;; batch-fetching all parent entities, adding overhead that two-phase avoids.
;;
;; **Note:** This is an XTDB 1.x limitation. XTDB 2.x may optimize `not-join`
;; differently, potentially making the Datalog-native approach competitive. Until
;; then, two-phase is the recommended pattern for relationship sensitivity filtering.
